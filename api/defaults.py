JAKEROSSBIO='''
I am the principal developer and maintainer for pychron, a data collection and processing application for noble gas geochronology and geochemistry. I am interested in pushing the boundaries of noble gas geochronology via advances in software and hardware. My research focuses on developing software and hardware interfaces to facilitate and ease noble gas experimentation. However, recently I have shifted focus from geochronology to using small Unmanned Aerial Systems (sUAS) for geoscience. The sUAS platforms we are developing are used for a variety of purposes, including generating detailed orthomosaics and 3D models, and collecting high-resolution thermal images and georeferenced environmental data.

In addition to my research at the New Mexico Bureau of Geology and the New Mexico Geochronology Research Laboratory, I work with two Hydrology groups on campus: 1) the NM Aquifer Mapping program developing data processing software tools and 2) the NMT EES Hydrology department mentoring students on scientific programming.
'''

JULIARICCIBIO='''
I joined the New Mexico Geochronology Research Laboratory as a research scientist and the Argon lab manager.

As a specialist in argon geochronology, I bring an essential temporal constraint to the understanding of 
different geological processes and their evolution in time and space. Using mainly volcanology, my research
projects have combined geochronology with different fields such as paleomagnetism, geomorphology and geochemistry. 
Thus, among other things, I studied, the volcanic evolution of the active islands, but also the evolution of the 
magnetic field during the last 5 Ma with focus on the time constraint of the last Matuyama-Brunhes magnetic inversion. 
More recently, I have worked on the improvement of the 40Ar/39Ar dating of clay minerals in order to better constrain 
the medium and low-temperature processes characteristic of earth (sub-) surface: e.g. hydrothermal systems 
(geothermal energy, mineral resources), fault system paleo-activities.

Thus, through technical development, I strive to provide the most accurate temporal constraints possible.

'''
